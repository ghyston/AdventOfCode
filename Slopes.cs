    public static int TreesOnSlopeCount(int right, int down)
    {
        var trees = 0;
        var width = Values.First().Count();

        for (int y = down, x = right; y < Values.Count(); y += down, x += right)
        {
            if (x >= width) x -= width;
            if (Values[y][x] == '#') trees++;        
        }
        return trees;
    }

